package com.hfocean.uavportal.console.airplan.vo;

/**
* 接收分页查询飞行计划申请参数
* @author Leslie.Lam
* @create 2017-06-25 21:04
**/
public class AirPlanPageParam {

    private Integer type;//	报告类型 0-管制 1-一类 2-二类(即空域类别)

    private String planType;//计划类型

    private String airArea;//空域所属行政区

    private String territorial;//所属空域辖区

    private String airName;//空域名称

    private String landing;//起降场类关键字，可能是广州、白云、越秀、大学城等字符

    private String air;//空域关键字, 可能是广州、白云、越秀、大学城等字符

    private String sort;//排序字段,多个字段间以逗号隔开 如sort=type,landing 默认使用时间降序

    private String code;//飞行计划申请号

    private String unit;//个人或单位名称(申请单位)

    private String startDate;//计划开始时间 格式：yyyy-MM-dd

    private String endDate;

    private Integer status;//状态

    private String map;

    public Integer getType() {
        return type;
    }

    public void setType(Integer type) {
        this.type = type;
    }

    public String getLanding() {
        return landing;
    }

    public void setLanding(String landing) {
        this.landing = landing;
    }

    public String getAir() {
        return air;
    }

    public void setAir(String air) {
        this.air = air;
    }

    public String getSort() {
        return sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getUnit() {
        return unit;
    }

    public void setUnit(String unit) {
        this.unit = unit;
    }

    public String getStartDate() {
        return startDate;
    }

    public void setStartDate(String startDate) {
        this.startDate = startDate;
    }

    public String getEndDate() {
        return endDate;
    }

    public void setEndDate(String endDate) {
        this.endDate = endDate;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getPlanType() {
        return planType;
    }

    public void setPlanType(String planType) {
        this.planType = planType;
    }

    public String getAirArea() {
        return airArea;
    }

    public void setAirArea(String airArea) {
        this.airArea = airArea;
    }

    public String getTerritorial() {
        return territorial;
    }

    public void setTerritorial(String territorial) {
        this.territorial = territorial;
    }

    public String getAirName() {
        return airName;
    }

    public void setAirName(String airName) {
        this.airName = airName;
    }

    public String getMap() {
        return map;
    }

    public void setMap(String map) {
        this.map = map;
    }
}
