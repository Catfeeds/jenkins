package com.hfocean.uavportal.core.content.enm;

public enum ContentEnum {
	
	/** status 1正常,2删除 */
	禁用(0),正常(1),删除(2),
	
	/** is_parent 是否为父类目(1true,0false)' */
	父类(1),子类(0),
	
	/** type 0菜单1稿件列表2稿件内容 */
	菜单(0),稿件列表(1),稿件内容(2),
	
	/**is_recommend 是否推荐(0不推荐,1推荐)*/
	不推荐(0),推荐(1)
	
	;
	
	private int code;
	
	private ContentEnum(int code){
		this.code = code;
	}
	public int getCode(){
		return this.code;
	}
	
}
